package Receipt;
public class OrderListArray {
	
	int no;
	String name;
	int price;
	int count;
	int total;

	
	
	public OrderListArray(int no, String name, int price, int count, int total) {
		this.no = no;
		this.name = name;
		this.price = price;
		this.price = price; 
		this.count = count;
		this.total = total;
	}	
}